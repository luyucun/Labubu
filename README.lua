注意：以下是本项目开发时的注意事项：

1.如果你要新增脚本文件，要在开头标注清楚脚本名字/脚本类型/脚本放置的位置，注意注释需要用中文，脚本名用英文
2.脚本类型必须写具体类型（ModuleScript/LocalScript/Script），不要写中文或额外说明
3.脚本放置位置必须写 Roblox Studio 内的位置（例如 ReplicatedStorage/Config/GameConfig）
4.不要在当前目录下创建更深的文件夹，所有新增文件放在 D:\RobloxGame\Labubu\Labubu 根目录
5.Studio中允许按架构建立文件夹，脚本文件由我手动同步到对应位置
6.如果是客户端脚本，尽量统一放置在StarterPlayerScripts中
7.全程使用中文跟我对话
8.你只需要进行开发即可，永远不要创建什么说明文档/部署文档/开发总结这类的没用的内容
9.这个游戏是基于roblox进行开发的，使用的lua语言
10.在开发新内容的时候，要先设计架构，架构要设计准确高效，然后依照架构进行执行


补充：
1.我们需要一个游戏的整体架构文档，每次开发新需求或者在修改bug或者重构代码的过程中，涉及到了架构的修改，要记得修改架构文档
2.架构文档尽量精简，少说废话，既要精简准确又不能遗漏内容
3.我们的RemoteEvent，是用来存放RemoteEvent说明的，每次要新增RemoteEvent时，要及时更正补充好RemoteEvent说明文档

